package phatnh.struts2;

import com.opensymphony.xwork2.ActionContext;
import java.util.Map;
import org.apache.struts2.convention.annotation.Action;
import org.apache.struts2.convention.annotation.ExceptionMapping;
import org.apache.struts2.convention.annotation.ExceptionMappings;
import org.apache.struts2.convention.annotation.Result;
import org.apache.struts2.convention.annotation.ResultPath;
import org.apache.struts2.convention.annotation.Results;
import phatnh.cart.CartDTO;

/**
 *
 * @author nguyenhongphat0
 */
@ResultPath("/")
@Results({
    @Result(type = "redirectAction", params = {
        "actionName", "searchPrice",
        "min", "${min}",
        "max", "${max}"
    })
})
public class DropFromCartAction {
    private String mobileId;
    private int min;
    private int max;

    public String getMobileId() {
        return mobileId;
    }

    public void setMobileId(String mobileId) {
        this.mobileId = mobileId;
    }

    public int getMin() {
        return min;
    }

    public void setMin(int min) {
        this.min = min;
    }

    public int getMax() {
        return max;
    }

    public void setMax(int max) {
        this.max = max;
    }
    
    public DropFromCartAction() {
    }
    
    @Action("dropFromCart")
    public String execute() throws Exception {
        Map session = ActionContext.getContext().getSession();
        CartDTO cart = (CartDTO) session.get("cart");
        if (cart != null) {
            cart.removeFromCart(mobileId);
        }
        return "success";
    }
    
}
